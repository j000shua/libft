#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*p;

	if (!s)
	       return (NULL);	
	p = s;
	while (n)
	{
		*p = c;
		n--;
		p++;
	}
	return (s);
}
